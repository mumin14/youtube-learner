// --- Calendar Settings Panel ---

function CalendarSettingsPanel({ onClose }: { onClose: () => void }) {
  const [settings, setSettings] = useState<CalendarSettings | null>(null);
  const [saving, setSaving] = useState(false);
  const [loadingSettings, setLoadingSettings] = useState(true);

  useEffect(() => {
    fetch("/api/calendar/settings")
      .then((r) => r.json())
      .then((data) => setSettings(data))
      .catch(() => toast.error("Failed to load settings"))
      .finally(() => setLoadingSettings(false));
  }, []);

  const handleSave = async () => {
    if (!settings) return;
    setSaving(true);
    try {
      const res = await fetch("/api/calendar/settings", {
        method: "PUT",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(settings),
      });
      if (!res.ok) {
        const data = await res.json();
        throw new Error(data.error || "Failed to save");
      }
      toast.success("Calendar settings saved");
      onClose();
    } catch (err) {
      toast.error(err instanceof Error ? err.message : "Failed to save settings");
    } finally {
      setSaving(false);
    }
  };

  const updateAvailability = (day: string, field: keyof DayAvailability, value: string | boolean) => {
    if (!settings) return;
    setSettings({
      ...settings,
      availability: {
        ...settings.availability,
        [day]: { ...settings.availability[day], [field]: value },
      },
    });
  };

  if (loadingSettings) {
    return (
      <div className="flex items-center justify-center py-20">
        <svg className="w-6 h-6 animate-spin text-muted-foreground" fill="none" viewBox="0 0 24 24">
          <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" />
          <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4z" />
        </svg>
      </div>
    );
  }

  if (!settings) return null;

  return (
    <div className="max-w-2xl">
      <div className="flex items-center justify-between mb-6">
        <div className="flex items-center gap-3">
          <button
            onClick={onClose}
            className="p-1.5 rounded-lg text-muted-foreground hover:text-foreground hover:bg-muted transition-colors"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
            </svg>
          </button>
          <h2 className="text-lg font-semibold">Calendar Settings</h2>
        </div>
        <Button onClick={handleSave} disabled={saving} className="rounded-lg">
          {saving ? "Saving..." : "Save Settings"}
        </Button>
      </div>

      <div className="space-y-6">
        {/* Timezone */}
        <Card>
          <CardContent className="p-4">
            <label className="block text-sm font-medium mb-2">Timezone</label>
            <select
              value={settings.timezone}
              onChange={(e) => setSettings({ ...settings, timezone: e.target.value })}
              className="w-full rounded-lg border border-border bg-background px-3 py-2 text-sm focus:outline-none focus:ring-2 focus:ring-primary/20"
            >
              {COMMON_TIMEZONES.map((tz) => (
                <option key={tz} value={tz}>{tz.replace(/_/g, " ")}</option>
              ))}
            </select>
            <p className="text-xs text-muted-foreground mt-1">
              Action items will be scheduled in this timezone
            </p>
          </CardContent>
        </Card>

        {/* Items per day */}
        <Card>
          <CardContent className="p-4">
            <label className="block text-sm font-medium mb-2">
              Maximum items per day: <span className="text-primary">{settings.items_per_day}</span>
            </label>
            <input
              type="range"
              min={1}
              max={15}
              value={settings.items_per_day}
              onChange={(e) => setSettings({ ...settings, items_per_day: Number(e.target.value) })}
              className="w-full accent-primary"
            />
            <div className="flex justify-between text-[10px] text-muted-foreground mt-1">
              <span>Light (1)</span>
              <span>Default (6)</span>
              <span>Intensive (15)</span>
            </div>
          </CardContent>
        </Card>

        {/* Weekly availability */}
        <Card>
          <CardContent className="p-4">
            <label className="block text-sm font-medium mb-3">Weekly Availability</label>
            <div className="space-y-2">
              {DAY_KEYS.map((day, i) => {
                const config = settings.availability[day];
                if (!config) return null;
                return (
                  <div
                    key={day}
                    className={`flex items-center gap-3 p-3 rounded-lg border transition-colors ${
                      config.enabled ? "border-border bg-card" : "border-border/50 bg-muted/30"
                    }`}
                  >
                    {/* Toggle */}
                    <button
                      onClick={() => updateAvailability(day, "enabled", !config.enabled)}
                      className={`w-10 h-5 rounded-full relative transition-colors shrink-0 ${
                        config.enabled ? "bg-primary" : "bg-muted-foreground/30"
                      }`}
                    >
                      <span
                        className={`absolute top-0.5 w-4 h-4 rounded-full bg-white shadow-sm transition-transform ${
                          config.enabled ? "left-[22px]" : "left-0.5"
                        }`}
                      />
                    </button>

                    {/* Day name */}
                    <span className={`text-sm font-medium w-24 capitalize ${
                      config.enabled ? "text-foreground" : "text-muted-foreground"
                    }`}>
                      {DAY_NAMES[i]}day
                    </span>

                    {/* Time inputs */}
                    {config.enabled ? (
                      <div className="flex items-center gap-2 ml-auto">
                        <input
                          type="time"
                          value={config.start}
                          onChange={(e) => updateAvailability(day, "start", e.target.value)}
                          className="text-sm px-2 py-1 rounded-md border border-border bg-background focus:outline-none focus:ring-1 focus:ring-primary/20"
                        />
                        <span className="text-muted-foreground text-sm">to</span>
                        <input
                          type="time"
                          value={config.end}
                          onChange={(e) => updateAvailability(day, "end", e.target.value)}
                          className="text-sm px-2 py-1 rounded-md border border-border bg-background focus:outline-none focus:ring-1 focus:ring-primary/20"
                        />
                      </div>
                    ) : (
                      <span className="text-xs text-muted-foreground ml-auto">Off</span>
                    )}
                  </div>
                );
              })}
            </div>
          </CardContent>
        </Card>
      </div>
    </div>
  );
}

// --- Week View ---

function WeekView({
  days,
  todayStr,
  itemsByDate,
  selectedDate,
  onSelectDate,
  loading,
}: {
  days: Date[];
  todayStr: string;
  itemsByDate: Record<string, ActionItem[]>;
  selectedDate: string | null;
  onSelectDate: (date: string) => void;
  loading: boolean;
}) {
  return (
    <div className="grid grid-cols-7 gap-2">
      {/* Day headers */}
      {DAY_NAMES.map((name) => (
        <div key={name} className="text-center text-xs font-medium text-muted-foreground py-2">
          {name}
        </div>
      ))}
      {/* Day cells */}
      {days.map((day) => {
        const dateStr = toDateStr(day);
        const isToday = dateStr === todayStr;
        const isSelected = dateStr === selectedDate;
        const dayItems = itemsByDate[dateStr] ?? [];

        return (
          <div
            key={dateStr}
            onClick={() => onSelectDate(dateStr)}
            className={`group rounded-xl border p-3 min-h-[140px] cursor-pointer transition-all ${
              isSelected
                ? "border-primary bg-primary/5 shadow-md"
                : isToday
                ? "border-primary/40 bg-primary/5"
                : "border-border bg-card hover:border-primary/30 hover:shadow-sm"
            }`}
          >
            <div className="flex items-center justify-between mb-2">
              <span
                className={`text-sm font-semibold ${
                  isToday
                    ? "text-primary"
                    : "text-foreground"
                }`}
              >
                {day.getDate()}
              </span>
              <div className="flex items-center gap-1">
                {dayItems.length > 0 && (
                  <>
                    <button
                      onClick={(e) => {
                        e.stopPropagation();
                        window.open(`/api/calendar/ics?date=${dateStr}`, "_blank");
                      }}
                      className="opacity-0 group-hover:opacity-100 p-0.5 rounded text-muted-foreground hover:text-primary transition-all"
                      title="Download .ics for this day"
                    >
                      <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
                      </svg>
                    </button>
                    <span className="text-[10px] font-medium text-muted-foreground bg-muted px-1.5 py-0.5 rounded-full">
                      {dayItems.length}
                    </span>
                  </>
                )}
              </div>
            </div>
            {loading ? (
              <div className="space-y-1.5">
                <div className="h-5 rounded bg-muted animate-pulse" />
                <div className="h-5 rounded bg-muted animate-pulse w-3/4" />
              </div>
            ) : (
              <div className="space-y-1">
                {dayItems.slice(0, 4).map((item) => {
                  const dc = DIFFICULTY_COLORS[item.difficulty] || DIFFICULTY_COLORS.easy;
                  return (
                    <div
                      key={item.id}
                      className={`flex items-center gap-1.5 px-2 py-1 rounded-md text-[11px] ${dc.bg} ${dc.text} ${
                        item.completed ? "opacity-50 line-through" : ""
                      }`}
                    >
                      <span className={`w-1.5 h-1.5 rounded-full shrink-0 ${dc.dot}`} />
                      {item.scheduled_time && (
                        <span className="text-[9px] opacity-70 shrink-0">
                          {formatTime12h(item.scheduled_time)}
                        </span>
                      )}
                      <span className="truncate">{item.title}</span>
                    </div>
                  );
                })}
                {dayItems.length > 4 && (
                  <p className="text-[10px] text-muted-foreground pl-1">
                    +{dayItems.length - 4} more
                  </p>
                )}
              </div>
            )}
          </div>
        );
      })}
    </div>
  );
}

// --- Month View ---

