// --- Day Detail Panel ---

function DayDetail({
  date,
  items,
  onClose,
  onToggleComplete,
  onReschedule,
}: {
  date: string;
  items: ActionItem[];
  onClose: () => void;
  onToggleComplete: (id: number, completed: boolean) => void;
  onReschedule: (id: number, date: string | null, time?: string | null) => void;
}) {
  const d = new Date(date + "T00:00:00");
  const label = d.toLocaleDateString("en-US", {
    weekday: "long",
    month: "long",
    day: "numeric",
  });

  const completedCount = items.filter((i) => i.completed).length;

  return (
    <Card className="w-80 shrink-0 border-border bg-card self-start sticky top-8">
      <CardContent className="p-4">
        <div className="flex items-center justify-between mb-4">
          <div>
            <h3 className="text-sm font-semibold">{label}</h3>
            <p className="text-xs text-muted-foreground">
              {items.length} {items.length === 1 ? "item" : "items"}
              {completedCount > 0 && ` (${completedCount} done)`}
            </p>
          </div>
          <button
            onClick={onClose}
            className="p-1 rounded-md text-muted-foreground hover:text-foreground hover:bg-muted transition-colors"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {items.length === 0 ? (
          <p className="text-sm text-muted-foreground text-center py-6">
            No items scheduled for this day
          </p>
        ) : (
          <div className="space-y-2">
            {items.map((item) => {
              const dc = DIFFICULTY_COLORS[item.difficulty] || DIFFICULTY_COLORS.easy;
              return (
                <div
                  key={item.id}
                  className={`rounded-lg border border-border p-3 transition-all ${
                    item.completed ? "opacity-60" : ""
                  }`}
                >
                  <div className="flex items-start gap-2">
                    <button
                      onClick={() => onToggleComplete(item.id, !item.completed)}
                      className={`shrink-0 mt-0.5 w-4 h-4 rounded border-2 flex items-center justify-center transition-colors ${
                        item.completed
                          ? "bg-primary border-primary"
                          : "border-border hover:border-primary/50"
                      }`}
                    >
                      {item.completed ? (
                        <svg className="w-2.5 h-2.5 text-primary-foreground" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={3} d="M5 13l4 4L19 7" />
                        </svg>
                      ) : null}
                    </button>
                    <div className="min-w-0 flex-1">
                      {/* Time display */}
                      {item.scheduled_time && (
                        <p className="text-[10px] font-medium text-primary mb-0.5">
                          {formatTime12h(item.scheduled_time)}
                        </p>
                      )}
                      <p className={`text-sm font-medium leading-snug ${item.completed ? "line-through" : ""}`}>
                        {item.title}
                      </p>
                      {item.description && (
                        <p className="text-xs text-muted-foreground mt-1 line-clamp-2">
                          {item.description}
                        </p>
                      )}
                      <div className="flex items-center gap-2 mt-2">
                        <span className={`inline-flex items-center gap-1 px-1.5 py-0.5 rounded text-[10px] font-medium ${dc.bg} ${dc.text}`}>
                          <span className={`w-1.5 h-1.5 rounded-full ${dc.dot}`} />
                          {item.difficulty}
                        </span>
                        {item.topic && (
                          <span className="text-[10px] text-muted-foreground bg-muted px-1.5 py-0.5 rounded">
                            {item.topic}
                          </span>
                        )}
                      </div>
                      {item.filename && (
                        <p className="text-[10px] text-muted-foreground/60 mt-1.5 truncate">
                          {item.source_type === "youtube" ? "\u25B6 " : ""}{item.filename}
                        </p>
                      )}
                    </div>
                  </div>
                  {/* Reschedule + Calendar export */}
                  <div className="flex items-center gap-1.5 mt-2 pt-2 border-t border-border">
                    <button
                      onClick={() => onReschedule(item.id, null)}
                      className="text-[10px] text-muted-foreground hover:text-foreground transition-colors"
                    >
                      Unschedule
                    </button>
                    <span className="text-muted-foreground/30">|</span>
                    <RescheduleInput
                      currentDate={date}
                      currentTime={item.scheduled_time ?? undefined}
                      onReschedule={(newDate, newTime) => onReschedule(item.id, newDate, newTime)}
                    />
                    <div className="ml-auto">
                      <ItemCalendarActions item={item} />
                    </div>
                  </div>
                </div>
              );
            })}
          </div>
        )}
      </CardContent>
    </Card>
  );
}

function RescheduleInput({
  currentDate,
  currentTime,
  onReschedule,
}: {
  currentDate: string;
  currentTime?: string;
  onReschedule: (date: string, time?: string) => void;
}) {
  const [editing, setEditing] = useState(false);
  const [newDate, setNewDate] = useState(currentDate);
  const [newTime, setNewTime] = useState(currentTime ?? "");

  if (!editing) {
    return (
      <button
        onClick={() => setEditing(true)}
        className="text-[10px] text-muted-foreground hover:text-foreground transition-colors"
      >
        Move to...
      </button>
    );
  }

  const handleConfirm = () => {
    if (newDate) {
      onReschedule(newDate, newTime || undefined);
      setEditing(false);
    }
  };

  return (
    <div className="flex items-center gap-1">
      <input
        type="date"
        value={newDate}
        autoFocus
        onChange={(e) => setNewDate(e.target.value)}
        className="text-[10px] px-1.5 py-0.5 rounded border border-border bg-background focus:outline-none focus:ring-1 focus:ring-primary/20"
      />
      <input
        type="time"
        value={newTime}
        onChange={(e) => setNewTime(e.target.value)}
        className="text-[10px] px-1.5 py-0.5 rounded border border-border bg-background focus:outline-none focus:ring-1 focus:ring-primary/20 w-20"
      />
      <button
        onClick={handleConfirm}
        className="text-[10px] text-primary hover:text-primary/80 font-medium transition-colors"
      >
        OK
      </button>
      <button
        onClick={() => setEditing(false)}
        className="text-[10px] text-muted-foreground hover:text-foreground transition-colors"
      >
        Cancel
      </button>
    </div>
  );
}
